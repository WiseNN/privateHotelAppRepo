package hotelbackend;

//credit card info class for customers
public class CreditCardInfo
{
    String nameOnCard;
    String cardNumber;
    String cardExprDate;
    String cvnCode;

    CreditCardInfo(String nameOnCard, String cardNumber, String cardExprDate, String cvnCode)
    {
        this.nameOnCard = nameOnCard;
        this.cardNumber = cardNumber;
        this.cardExprDate = cardExprDate;
        this.cvnCode = cvnCode;

    }

}







